    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String input = jTextArea1.getText();

        if (jComboBox1.getSelectedItem().equals("Aggiungi")) {
            if (input.equals("")) {
                throw new IllegalArgumentException();
            } else {
                String[] data = input.split(ch);

                String matricola = data[0];
                String nome = data[1];
                String cognome = data[2];
                String codiceDisciplina = data[3];
                String newLine = matricola + "," + nome + "," + cognome + "," + codiceDisciplina;

                Docente nuovoDocente = new Docente(matricola, nome, cognome, codiceDisciplina);
                docenti.add(nuovoDocente);
                scriviFileCSV();

            }
        }

        if (jComboBox1.getSelectedItem().equals("Modifica")) {
            if (input.equals("")) {
                throw new IllegalArgumentException();
            } else {
                String[] data = input.split(",");

                if (data.length < 4) {
                    throw new IllegalArgumentException("I dati inseriti non sono validi.");
                }

                String matricolaDaModificare = data[0].trim();
                String nuovoNome = data[1].trim();
                String nuovoCognome = data[2].trim();
                String nuovoCodiceDisciplina = data[3].trim();

                boolean trovato = false;

                // Cerca il corso e modifica i suoi dati
                for (int i = 0; i < docenti.size(); i++) {
                    Docente docente = docenti.get(i);
                    if (docente.getMatricola().equals(matricolaDaModificare)) {
                        docente.setNome(nuovoNome);
                        docente.setCodiceDisciplina(nuovoCodiceDisciplina);
                        trovato = true;
                        break;
                    }
                }

                if (trovato) {
                    scriviFileCSV();
                } else {
                    throw new IllegalArgumentException("Docente non trovato con codice: " + matricolaDaModificare);
                }
            }
        }

        if (jComboBox1.getSelectedItem().equals("Elimina")) {
            if (input.equals("")) {
                throw new IllegalArgumentException();
            } else {
                String matricolaDaEliminare = input.trim();

                boolean trovato = false;

                // Usa il ciclo for per trovare e rimuovere il corso
                for (int i = 0; i < docenti.size(); i++) {
                    Docente docente = docenti.get(i);
                    if (docente.getMatricola().equals(matricolaDaEliminare)) {
                        docenti.remove(i);  // Rimuovi il corso
                        trovato = true;
                        break;  // Esci dal ciclo una volta che il corso Ã¨ stato rimosso
                    }
                }

                if (trovato) {
                    scriviFileCSV();
                } else {
                    throw new IllegalArgumentException("Docente non trovato con matricola: " + matricolaDaEliminare);
                }
            }
        }

        if ((jComboBox1.getSelectedItem().equals("ContaDocenti"))) {
            mostraNumeroDocenti();

        }


    }//GEN-LAST:event_jButton1ActionPerformed

