    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String input = jTextArea1.getText();

        if (jComboBox1.getSelectedItem().equals("Aggiungi")) {
            if (input.equals("")) {
                throw new IllegalArgumentException();
            } else {
                String[] data = input.split(ch);
                String codice = data[0];
                String nome = data[1];
                int durata = Integer.parseInt(data[2].substring(1, 2));
                String newLine = codice + "," + nome + "," + durata;

                CorsoUni nuovoCorso = new CorsoUni(codice, nome, durata);
                corsi.add(nuovoCorso);

                // Riscrivi l'ArrayList nel file CSV
                scriviFileCSV();
            }
        }

        if (jComboBox1.getSelectedItem().equals("Modifica")) {
            if (input.equals("")) {
                throw new IllegalArgumentException();
            } else {
                String[] data = input.split(",");

                if (data.length < 3) {
                    throw new IllegalArgumentException("I dati inseriti non sono validi.");
                }

                String codiceDaModificare = data[0].trim();
                String nuovoNome = data[1].trim();
                int nuovaDurata = Integer.parseInt(data[2].trim());

                boolean trovato = false;

                // Cerca il corso e modifica i suoi dati
                for (int i = 0; i < corsi.size(); i++) {
                    CorsoUni corso = corsi.get(i);
                    if (corso.getCodice().equals(codiceDaModificare)) {
                        corso.setNome(nuovoNome);
                        corso.setDurata(nuovaDurata);
                        trovato = true;
                        break;
                    }
                }

                if (trovato) {
                    scriviFileCSV();
                } else {
                    throw new IllegalArgumentException("Corso non trovato con codice: " + codiceDaModificare);
                }
            }
        }

        if (jComboBox1.getSelectedItem().equals("Elimina")) {
            if (input.equals("")) {
                throw new IllegalArgumentException();
            } else {
                String codiceDaEliminare = input.trim();

                boolean trovato = false;

                // Usa il ciclo for per trovare e rimuovere il corso
                for (int i = 0; i < corsi.size(); i++) {
                    CorsoUni corso = corsi.get(i);
                    if (corso.getCodice().equals(codiceDaEliminare)) {
                        corsi.remove(i);  // Rimuovi il corso
                        trovato = true;
                        break;  // Esci dal ciclo una volta che il corso Ã¨ stato rimosso
                    }
                }

                if (trovato) {
                    scriviFileCSV();
                } else {
                    throw new IllegalArgumentException("Corso non trovato con codice: " + codiceDaEliminare);
                }
            }
           
        }
        if ((jComboBox1.getSelectedItem().equals("ContaCorsi"))) {
                mostraNumeroCorsi();
            }
    
    }//GEN-LAST:event_jButton1ActionPerformed

