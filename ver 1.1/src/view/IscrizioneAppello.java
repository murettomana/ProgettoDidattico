    String ch = ",";
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String input = jTextArea1.getText();

        if (jComboBox1.getSelectedItem().equals("Aggiungi")) {
            if (input.equals("")) {
                throw new IllegalArgumentException();
            } else {
                String[] data = input.split(ch);
                String idAppello = data[0];
                String matricolaStudente = data[1];

                String newLine = idAppello + "," + matricolaStudente;

                Iscrizione nuovaIscrizione = new Iscrizione(idAppello, matricolaStudente);
                iscrizioni.add(nuovaIscrizione);
                scriviFileCSV();

            }
        }
        if (jComboBox1.getSelectedItem().equals("Modifica")) {
            if (input.equals("")) {
                throw new IllegalArgumentException();
            } else {
                String[] data = input.split(",");

                if (data.length < 2) {
                    throw new IllegalArgumentException("I dati inseriti non sono validi.");
                }

                String IDdaModificare = data[0].trim();
                String nuovamatricolaStudente = data[1].trim();

                boolean trovato = false;

                // Cerca il corso e modifica i suoi dati
                for (int i = 0; i < iscrizioni.size(); i++) {
                    Iscrizione iscrizione = iscrizioni.get(i);
                    if (iscrizione.getIdAppello().equals(IDdaModificare)) {
                        iscrizione.setMatricolaStudente(nuovamatricolaStudente);
                        trovato = true;
                        break;
                    }
                }

                if (trovato) {
                    scriviFileCSV();
                } else {
                    throw new IllegalArgumentException("Iscrizione non trovato con ID: " + IDdaModificare);
                }
            }
        }

        if (jComboBox1.getSelectedItem().equals("Elimina")) {
            if (input.equals("")) {
                throw new IllegalArgumentException();
            } else {
                String IDdaEliminare = input.trim();

                boolean trovato = false;

                // Usa il ciclo for per trovare e rimuovere il corso
                for (int i = 0; i < iscrizioni.size(); i++) {
                    Iscrizione iscrizione = iscrizioni.get(i);
                    if (iscrizione.getIdAppello().equals(IDdaEliminare)) {
                        iscrizioni.remove(i);  // Rimuovi il corso
                        trovato = true;
                        break;  // Esci dal ciclo una volta che il corso Ã¨ stato rimosso
                    }
                }

                if (trovato) {
                    scriviFileCSV();
                } else {
                    throw new IllegalArgumentException("Iscrizione non trovato con ID: " + IDdaEliminare);
                }
            }
        }
        if ((jComboBox1.getSelectedItem().equals("ContaIscrizioni"))) {
            mostraNumeroIscrizioni();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

