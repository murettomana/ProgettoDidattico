    String ch = ";";
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String input = jTextArea1.getText();

        if (jComboBox1.getSelectedItem().equals("Aggiungi")) {
            String[] dati = input.split(ch);
            String matricolaStudente = dati[0];
            String nome = dati[1];
            String cognome = dati[2];
            String codiceCorso = dati[3];
            ArrayList<AppelloEsame> appelli = new ArrayList<>();
            ArrayList<Disciplina> disciplina = new ArrayList<>();
            String newLine = matricolaStudente + "," + nome + "," + cognome + "," + codiceCorso;

            Studente nuovoStudente = new Studente(matricolaStudente, nome, cognome, codiceCorso, appelli, disciplina);
            studenti.add(nuovoStudente);
            scriviFileCSV();
        }

        if (jComboBox1.getSelectedItem().equals("Modifica")) {
            String[] data = input.split(",");

            if (data.length < 6) {
                throw new IllegalArgumentException("I dati inseriti non sono validi.");
            }

            String matricolaDaModificare = data[0].trim();
            String nuovoNome = data[1].trim();
            String nuovoCognome = data[2].trim();
            String nuovocodiceCorso = data[3].trim();
            ArrayList<AppelloEsame> newappelli = new ArrayList<>();
            ArrayList<Disciplina> newdisciplina = new ArrayList<>();

            boolean trovato = false;

            // Cerca il corso e modifica i suoi dati
            for (int i = 0; i < studenti.size(); i++) {
                Studente studente = studenti.get(i);
                if (studente.getMatricola().equals(matricolaDaModificare)) {
                    studente.setNome(nuovoNome);
                    studente.setCognome(nuovoCognome);
                    studente.setCodiceCorso(nuovocodiceCorso);
                    studente.setAppelli(newappelli);
                    studente.setDiscipline(newdisciplina);
                    trovato = true;
                    break;
                }
            }

            if (trovato) {
                scriviFileCSV();
            } else {
                throw new IllegalArgumentException("Studente non trovato con matricola: " + matricolaDaModificare);
            }
        }

        if (jComboBox1.getSelectedItem().equals("Elimina")) {
            String matricolaDaEliminare = input.trim();

            boolean trovato = false;

            // Usa il ciclo for per trovare e rimuovere il corso
            for (int i = 0; i < studenti.size(); i++) {
                Studente studente = studenti.get(i);
                if (studente.getMatricola().equals(matricolaDaEliminare)) {
                    studenti.remove(i);  // Rimuovi il corso
                    trovato = true;
                    break;  // Esci dal ciclo una volta che il corso Ã¨ stato rimosso
                }
            }

            if (trovato) {
                scriviFileCSV();
            } else {
                throw new IllegalArgumentException("Studente non trovato con matricola: " + matricolaDaEliminare);
            }
        }

        if ((jComboBox1.getSelectedItem().equals("ContaStudenti"))) {
            mostraNumeroStudenti();

        }


    }//GEN-LAST:event_jButton1ActionPerformed

