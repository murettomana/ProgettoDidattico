    String ch = ",";
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String input = jTextArea1.getText();

        if (jComboBox1.getSelectedItem().equals("Aggiungi")) {
            if (input.equals("")) {
                throw new IllegalArgumentException();
            } else {
                String[] data = input.split(ch);
                String codice = data[0];
                String nome = data[1];
                int cfu = Integer.parseInt(data[2].substring(1, 2));
                String codiceCorso = data[3];
                ArrayList<Docente> docentii = new ArrayList<>();
                ArrayList<AppelloEsame> appello = new ArrayList<>();
                ArrayList<Studente> studenti = new ArrayList<>();

                Disciplina nuovaDisciplina = new Disciplina(codice, nome, cfu, codiceCorso, docentii, appello, studenti);
                discipline.add(nuovaDisciplina);

                // Riscrivi l'ArrayList nel file CSV
                scriviFileCSV();
            }

        }

        if (jComboBox1.getSelectedItem().equals("Modifica")) {
            if (input.equals("")) {
                throw new IllegalArgumentException();
            } else {
                String[] data = input.split(",");

                if (data.length < 4) {
                    throw new IllegalArgumentException("I dati inseriti non sono validi.");
                }

                String codiceDaModificare = data[0].trim();
                String nuovoNome = data[1].trim();
                int nuovocfu = Integer.parseInt(data[2].substring(1, 2));
                String codiceCorso = data[3];
                ArrayList<Docente> newdocenti = new ArrayList<>();
                ArrayList<AppelloEsame> newappello = new ArrayList<>();
                ArrayList<Studente> newstudenti = new ArrayList<>();
                boolean trovato = false;

                // Cerca il corso e modifica i suoi dati
                for (int i = 0; i < discipline.size(); i++) {
                    Disciplina disciplina = discipline.get(i);
                    if (disciplina.getCodice().equals(codiceDaModificare)) {
                        disciplina.setNome(nuovoNome);
                        disciplina.setCFU(nuovocfu);
                        disciplina.setDocenti(newdocenti);
                        disciplina.setAppelli(newappello);
                        disciplina.setStudenti(newstudenti);
                        trovato = true;
                        break;
                    }
                }

                if (trovato) {
                    scriviFileCSV();
                } else {
                    throw new IllegalArgumentException("Disciplina non trovata con codice: " + codiceDaModificare);
                }
            }
        }

        if (jComboBox1.getSelectedItem().equals("Elimina")) {
            if (input.equals("")) {
                throw new IllegalArgumentException();
            } else {
                String codiceDaEliminare = input.trim();

                boolean trovato = false;

                // Usa il ciclo for per trovare e rimuovere il corso
                for (int i = 0; i < discipline.size(); i++) {
                    Disciplina disciplina = discipline.get(i);
                    if (disciplina.getCodice().equals(codiceDaEliminare)) {
                        discipline.remove(i);  // Rimuovi il corso
                        trovato = true;
                        break;  // Esci dal ciclo una volta che il corso Ã¨ stato rimosso
                    }
                }

                if (trovato) {
                    scriviFileCSV();
                } else {
                    throw new IllegalArgumentException("Disciplina non trovato con codice: " + codiceDaEliminare);
                }

            }
        }
        if ((jComboBox1.getSelectedItem().equals("ContaDiscipline"))) {
            mostraNumeroDiscipline();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

