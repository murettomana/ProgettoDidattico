    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String id = Id.getText();
        String data = Data.getText();
        String codice = codiceDisciplina.getText();
        boolean procedi = true;
        boolean presente = false;

        Border bordered = BorderFactory.createLineBorder(Color.RED, 2);

        if (codice.length() == 4 && codice.charAt(0) == 'D' && Character.isDigit(codice.charAt(1)) && Character.isDigit(codice.charAt(2)) && Character.isDigit(codice.charAt(3))) {
            codiceDisciplina.setBorder(null);
        } else {
            codiceDisciplina.setBorder(bordered);
            procedi = false;
        }

        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        try {
            LocalDate.parse(data, formatter);
            Data.setBorder(null);

        } catch (DateTimeParseException e) {
            Data.setBorder(bordered);
            procedi = false;
        }

        if (data.equals("")) {
            Data.setBorder(bordered);
        }

        if (id.length() == 3 && id.charAt(0) == 'A' && Character.isDigit(id.charAt(1)) && Character.isDigit(id.charAt(2))) {
            Id.setBorder(null);
        } else {
            Id.setBorder(bordered);
            procedi = false;
        }

        if (procedi == true) {
            if (jComboBox1.getSelectedItem().equals("Aggiungi")) {

                for (AppelloEsame a : appelli) {
                    String s1 = a.getIdAppello().replaceAll("\\p{Cntrl}", "");
                    
                    if (s1.equals(id)) {    // NON FUNZIONA CONTROLLARE IL PERCHÃ¨ DOPO
                        presente = true;
                        Id.setBorder(bordered);
                    }
                }

                if (presente == false) {
                    Id.setBorder(null);
                    String newLine = id + "," + data + "," + codice;
                    AppelloEsame nuovoAppello = new AppelloEsame(id, data, codice);
                    appelli.add(nuovoAppello);
                    G.salvaAppelliFIle();
                }
            }

            if (jComboBox1.getSelectedItem().equals("Modifica")) {
                int indice = 0;
                for (AppelloEsame a : appelli) {
                    String s1 = a.getIdAppello().replaceAll("\\p{Cntrl}", "");
                    
                    if (s1.equals(id)) {
                        appelli.set(indice, new AppelloEsame(id, data, codice)); // aggiorna il nome
                        G.salvaAppelliFIle();
                        return; // esce dal metodo una volta aggiornato
                    }
                    indice++;
                }
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

