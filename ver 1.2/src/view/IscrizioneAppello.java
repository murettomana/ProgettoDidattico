    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = idAppello.getText();
        String matricola = matricolaStudente.getText();

        boolean procedi = true;
        boolean presente = false;

        Border bordered = BorderFactory.createLineBorder(Color.RED, 2);

        if (id.length() == 3 && id.charAt(0) == 'A' && Character.isDigit(id.charAt(1)) && Character.isDigit(id.charAt(2))) {
            idAppello.setBorder(null);
        } else {
            idAppello.setBorder(bordered);
            procedi = false;
        }

        if (matricola.length() == 4 && matricola.charAt(0) == 'S' && Character.isDigit(matricola.charAt(1)) && Character.isDigit(matricola.charAt(2)) && Character.isDigit(matricola.charAt(3))) {
            matricolaStudente.setBorder(null);
        } else {
            matricolaStudente.setBorder(bordered);
            procedi = false;
        }

        if (procedi == true) {

            if (jComboBox1.getSelectedItem().equals("Aggiungi")) {

                for (Iscrizione i : iscrizioni) {
                    String s1 = i.getIdAppello().replaceAll("\\p{Cntrl}", "");
                    if (s1.equals(id)) {    // NON FUNZIONA CONTROLLARE IL PERCHÃ¨ DOPO
                        presente = true;
                        idAppello.setBorder(bordered);
                    }
                }

                if (presente == false) {
                    idAppello.setBorder(null);

                    String newLine = id + "," + matricola;

                    Iscrizione nuovaIscrizione = new Iscrizione(id, matricola);
                    iscrizioni.add(nuovaIscrizione);
                    G.salvaIscrizioniFIle();
                }
            }
            if (jComboBox1.getSelectedItem().equals("Modifica")) {
                int indice = 0;
                for (Iscrizione i : iscrizioni) {
                    String s1 = i.getIdAppello().replaceAll("\\p{Cntrl}", "");

                    if (s1.equals(id)) {
                        iscrizioni.set(indice, new Iscrizione(id, matricola)); // aggiorna il nome
                        G.salvaIscrizioniFIle();
                        return; // esce dal metodo una volta aggiornato
                    }
                    indice++;
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

