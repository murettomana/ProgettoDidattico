    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String codice = Codice.getText();
        String nome = Nome.getText();
        int anni = 0;
        boolean procedi = true;
        boolean presente = false;

        Border bordered = BorderFactory.createLineBorder(Color.RED, 2);

        if (codice.length() == 4 && codice.charAt(0) == 'C' && Character.isDigit(codice.charAt(1)) && Character.isDigit(codice.charAt(2)) && Character.isDigit(codice.charAt(3))) {
            Codice.setBorder(null);
        } else {
            Codice.setBorder(bordered);
            procedi = false;
        }

        if (nome.equals("")) {
            Nome.setBorder(bordered);
            procedi = false;
        } else {
            Nome.setBorder(null);
        }

        try {
            Integer.parseInt(Anni.getText());
            anni = Integer.parseInt(Anni.getText());
            Anni.setBorder(null);
        } catch (NumberFormatException e) {
            Anni.setBorder(bordered);
            procedi = false;
        }

        if (procedi == true) {
            if (jComboBox1.getSelectedItem().equals("Aggiungi")) {

                for (CorsoUni c : corsi) {
                    String s1 = c.getCodice().replaceAll("\\p{Cntrl}", "");

                    if (s1.equals(codice)) {    // NON FUNZIONA CONTROLLARE IL PERCHÃ¨ DOPO
                        presente = true;
                        Codice.setBorder(bordered);
                    }
                }

                if (presente == false) {

                    String newLine = codice + "," + nome + "," + anni;

                    CorsoUni nuovoCorso = new CorsoUni(codice, nome, anni);
                    corsi.add(nuovoCorso);

                    // Riscrivi l'ArrayList nel file CSV
                    G.salvaCorsiFIle();
                }
            }

            if (jComboBox1.getSelectedItem().equals("Modifica")) {
                int indice = 0;
                for (CorsoUni c : corsi) {
                    String s1 = c.getCodice().replaceAll("\\p{Cntrl}", "");
                    if (s1.equals(codice)) {
                        corsi.set(indice, new CorsoUni(codice, nome, anni)); // aggiorna il nome
                        G.salvaCorsiFIle();
                        return; // esce dal metodo una volta aggiornato
                    }
                    indice++;
                }
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

